public class Huluwa {
    private int no;
    private String name;
    private String color;

    // constructor
    Huluwa(int no, String name, String color) {
        this.no = no;
        this.name = name;
        this.color = color;
    }

    // get Huluwa's no
    public int getNo() {
        return no;
    }

    // get Huluwa's name
    public String getName() {
        return name;
    }

    // get Huluwa's color
    public String getColor() {
        return color;
    }
}
